namespace NinjaTrader.NinjaScript.Strategies
{
	public class WaddahAttarEntry : Strategy
	{
		private bool Long;
		private bool Short;
		private bool SetPTSL;
		private bool SetTrail;
		private bool TradeON;

		private NinjaTrader.NinjaScript.Indicators.NMN.WAE_Mod WaddahAttarExplosion1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "WaddahAttarEntry";
				Calculate									= Calculate.OnPriceChange;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				ProfitShort					= -40;
				StopLong					= -40;
				ProfitLong					= 40;
				StopShort					= 40;
				TrailTrigger					= 20;
				Trail					= 20;
				Long					= false;
				Short					= false;
				SetPTSL					= false;
				SetTrail					= false;
				TradeON					= false;
			}
			else if (State == State.Configure)
			{
			}
			else if (State == State.DataLoaded)
			{				
				WaddahAttarExplosion1				= WAE_Mod(Close, 150, 10, true, 9, 30, true, 9, 30, 2, 200);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 1)
				return;

			 // Set 1
			if ((Position.MarketPosition == MarketPosition.Flat)
				 && (State == State.Realtime)
				 && (CurrentBars[0] > 20))
			{
				TradeON = true;
			}
			
			 // Set 2
			if ((TradeON == true)
				 && (CrossAbove(WaddahAttarExplosion1.TrendUp, WaddahAttarExplosion1.ExplosionLine, 1)))
			{
				EnterLong(Convert.ToInt32(DefaultQuantity), @"myLong");
				SetPTSL = true;
				TradeON = false;
			}
			
			 // Set 3
			if ((TradeON == true)
				 && (CrossBelow(WaddahAttarExplosion1.TrendDown, WaddahAttarExplosion1.ExplosionLineDn, 1)))
			{
				EnterShort(Convert.ToInt32(DefaultQuantity), @"myShort");
				SetPTSL = true;
				TradeON = false;
			}
			
			 // Set 4
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (SetPTSL == true))
			{
				ExitLongLimit(Convert.ToInt32(DefaultQuantity), (Position.AveragePrice + (ProfitLong * TickSize)) , @"myTarget", @"myLong");
				ExitLongStopLimit(Convert.ToInt32(DefaultQuantity), 0, (Position.AveragePrice + (StopLong * TickSize)) , @"myStop", @"myLong");
				SetPTSL = false;
			}
			
			 // Set 5
			if ((Position.MarketPosition == MarketPosition.Short)
				 && (SetPTSL == true))
			{
				ExitShortLimit(Convert.ToInt32(DefaultQuantity), (Position.AveragePrice + (ProfitShort * TickSize)) , @"myTarget", @"myShort");
				ExitShortStopLimit(Convert.ToInt32(DefaultQuantity), (Position.AveragePrice + (StopShort * TickSize)) , 0, @"myShort", @"myShort");
				SetPTSL = false;
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Display(Name="ProfitShort", Order=1, GroupName="Parameters")]
		public int ProfitShort
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="StopLong", Order=2, GroupName="Parameters")]
		public int StopLong
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="ProfitLong", Order=3, GroupName="Parameters")]
		public int ProfitLong
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="StopShort", Order=4, GroupName="Parameters")]
		public int StopShort
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="TrailTrigger", Description="Trailing Trigger price", Order=5, GroupName="Parameters")]
		public int TrailTrigger
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="Trail", Description="Trailing behind amount", Order=6, GroupName="Parameters")]
		public int Trail
		{ get; set; }
		#endregion

	}
