namespace NinjaTrader.NinjaScript.Strategies
{
	public class DEMAxSMA : Strategy
	{
		private bool SLTPset;

		private DEMA DEMA1;
		private SMA SMA1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"DEMA-14   SMA-10 crossing entry";
				Name										= "DEMAxSMA";
				Calculate									= Calculate.OnPriceChange;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				EnclosingBar					= 1;
				LookBack					= 1;
				ProfitTarget					= 50;
				StopLoss					= -50;
				SLTPset					= false;
			}
			else if (State == State.Configure)
			{
			}
			else if (State == State.DataLoaded)
			{				
				DEMA1				= DEMA(Close, 14);
				SMA1				= SMA(Close, 10);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 3)
				return;

			 // Set 1
			if ((Position.MarketPosition == MarketPosition.Flat)
				 // Green enclosing bar for long entry
				 && ((Close[1] > Open[1])
				 && (Close[1] > DEMA1[1])
				 && (Close[1] > SMA1[1])
				 && (Open[1] < DEMA1[1])
				 && (Open[1] < SMA1[1]))
				 && (CrossAbove(DEMA1, SMA1, 3)))
			{
				EnterLongLimit(Convert.ToInt32(DefaultQuantity), (GetCurrentAsk(0) + (1 * TickSize)) , @"DSLongEntry");
				Print(@"checking  Long entry conditions");
			}
			
			 // Set 2
			if ((Position.MarketPosition == MarketPosition.Flat)
				 // Red enclosing bar for short entry
				 && ((Close[1] < Open[1])
				 && (Close[1] < DEMA1[1])
				 && (Close[1] < SMA1[1])
				 && (Open[1] > DEMA1[1])
				 && (Open[1] > SMA1[1]))
				 && (CrossBelow(DEMA1, SMA1, 3)))
			{
				EnterShortLimit(Convert.ToInt32(DefaultQuantity), (GetCurrentBid(0) + (1 * TickSize)) , @"DSShortEntry");
			}
			
			 // Set 3
			if ((Position.MarketPosition == MarketPosition.Long)
				 && (SLTPset == false))
			{
				ExitLongStopLimit(Convert.ToInt32(DefaultQuantity), (Close[0] + (StopLoss * TickSize)) , (Close[0] + (StopLoss * TickSize)) , @"DSLongSL", @"DSLongEntry");
				ExitLongLimit(Convert.ToInt32(DefaultQuantity), (Close[0] + (ProfitTarget * TickSize)) , @"DSLongPT", @"DSLongEntry");
				SLTPset = true;
			}
			
			 // Set 4
			if ((Position.MarketPosition == MarketPosition.Short)
				 && (SLTPset == false))
			{
				ExitShortStopLimit(Convert.ToInt32(DefaultQuantity), (Close[0] + (ProfitTarget * TickSize)) , (Close[0] + (StopLoss * TickSize)) , @"DSShortPT", @"DSShortEntry");
				ExitShortLimit(Convert.ToInt32(DefaultQuantity), (Close[0] + (StopLoss * TickSize)) , @"DSShortSL", @"DSShortEntry");
				SLTPset = true;
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="EnclosingBar", Description="Bar that crossed the DEMA SMA together", Order=1, GroupName="Parameters")]
		public int EnclosingBar
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="LookBack", Description="Crossing Lookback number of bars", Order=2, GroupName="Parameters")]
		public int LookBack
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="ProfitTarget", Description="Profit Target in ticks", Order=3, GroupName="Parameters")]
		public int ProfitTarget
		{ get; set; }

		[NinjaScriptProperty]
		[Display(Name="StopLoss", Description="Stop Loss in ticks", Order=4, GroupName="Parameters")]
		public int StopLoss
		{ get; set; }
		#endregion

	}
}

