namespace NinjaTrader.NinjaScript.Strategies
{
	public class MyTestStrat : Strategy
	{
		private bool BullBarEnclose;
		private bool BearBarEnclose;

		private DEMA DEMA1;
		private SMA SMA1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "MyTestStrat";
				Calculate									= Calculate.OnPriceChange;
				EntriesPerDirection							= 1;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= false;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				BullBarEnclose					= false;
				BearBarEnclose					= false;
			}
			else if (State == State.Configure)
			{
			}
			else if (State == State.DataLoaded)
			{				
				DEMA1				= DEMA(Close, 14);
				SMA1				= SMA(Close, 10);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 3)
				return;

			 // Set 1
			if ((BullBarEnclose == true)
				 && (CrossAbove(DEMA1, SMA1, 3)))
			{
				BarBrush = Brushes.DarkGreen;
				BullBarEnclose = false;
			}
			
			 // Set 2
			if (
				 // Bull bar 1 enclosed
				((Close[1] > Open[1])
				 && (Close[1] > DEMA1[1])
				 && (Close[1] > SMA1[1])
				 && (Open[1] < DEMA1[1])
				 && (Open[1] < SMA1[1])
				 && (Close[0] > Open[0]))
				 // Bull bar 2 enclosed
				 || ((Close[2] > Open[2])
				 && (Close[2] > DEMA1[2])
				 && (Close[2] > SMA1[2])
				 && (Open[2] < DEMA1[2])
				 && (Open[2] < SMA1[2])
				 && (Close[0] > Open[0]))
				 // Bull bar 3 enclosed
				 || ((Close[3] > Open[3])
				 && (Close[3] > DEMA1[3])
				 && (Close[3] > SMA1[3])
				 && (Open[3] < DEMA1[3])
				 && (Open[3] < SMA1[3])
				 && (Close[0] > Open[0])))
			{
				BullBarEnclose = true;
			}
			
			 // Set 3
			if (
				 // Bear bar 1 enclosed
				((Close[1] < Open[1])
				 && (Close[1] < DEMA1[1])
				 && (Close[1] < SMA1[1])
				 && (Open[1] > DEMA1[1])
				 && (Open[1] > SMA1[1])
				 && (Close[0] < Open[0]))
				 // Bear bar 2 enclosed
				 || ((Close[2] < Open[2])
				 && (Close[2] < DEMA1[2])
				 && (Close[2] < SMA1[2])
				 && (Open[2] > DEMA1[2])
				 && (Open[2] > SMA1[2])
				 && (Close[0] < Open[0]))
				 // Bear bar 3 enclosed
				 || ((Close[3] < Open[3])
				 && (Close[3] < DEMA1[3])
				 && (Close[3] < SMA1[3])
				 && (Open[3] > DEMA1[3])
				 && (Open[3] > SMA1[3])
				 && (Close[0] < Open[0])))
			{
				BearBarEnclose = true;
			}
			
			 // Set 4
			if ((BearBarEnclose == true)
				 && (CrossBelow(DEMA1, SMA1, 3)))
			{
				BarBrush = Brushes.Red;
				BearBarEnclose = false;
			}
			
		}
	}
}

