namespace NinjaTrader.NinjaScript.Strategies.Numan
{
	public class DEMAEntry : Strategy
	{
		private DEMA DEMA1;

		protected override void OnStateChange()
		{
			if (State == State.SetDefaults)
			{
				Description									= @"Enter the description for your new custom Strategy here.";
				Name										= "DEMAEntry";
				Calculate									= Calculate.OnBarClose;
				EntriesPerDirection							= 3;
				EntryHandling								= EntryHandling.AllEntries;
				IsExitOnSessionCloseStrategy				= true;
				ExitOnSessionCloseSeconds					= 30;
				IsFillLimitOnTouch							= false;
				MaximumBarsLookBack							= MaximumBarsLookBack.TwoHundredFiftySix;
				OrderFillResolution							= OrderFillResolution.Standard;
				Slippage									= 0;
				StartBehavior								= StartBehavior.WaitUntilFlat;
				TimeInForce									= TimeInForce.Gtc;
				TraceOrders									= true;
				RealtimeErrorHandling						= RealtimeErrorHandling.StopCancelClose;
				StopTargetHandling							= StopTargetHandling.PerEntryExecution;
				BarsRequiredToTrade							= 20;
				// Disable this property for performance gains in Strategy Analyzer optimizations
				// See the Help Guide for additional information
				IsInstantiatedOnEachOptimizationIteration	= true;
				Target1					= 10;
				Target2					= 20;
				Stop					= 10;
				Size1					= 1;
				Size2					= 1;
			}
			else if (State == State.Configure)
			{
			}
			else if (State == State.DataLoaded)
			{				
				DEMA1				= DEMA(Close, 14);
				SetProfitTarget(@"Pos1", CalculationMode.Ticks, Target1);
				SetTrailStop(@"Pos2", CalculationMode.Ticks, Stop, false);
				SetStopLoss(@"Pos1", CalculationMode.Ticks, Stop, false);
				SetProfitTarget(@"Pos2", CalculationMode.Ticks, Target2);
			}
		}

		protected override void OnBarUpdate()
		{
			if (BarsInProgress != 0) 
				return;

			if (CurrentBars[0] < 5)
				return;

			 // Set 1
			if (Position.MarketPosition != MarketPosition.Flat)
			{
				Print(Convert.ToString(Times[0][0]) + @":Bar Since entry = :" + Convert.ToString(BarsSinceEntryExecution(0, "", 0)));
			}
			
			 // Set 2
			if (
				 // Market Position -- Long
				(Position.MarketPosition == MarketPosition.Long)
				 && (CrossBelow(Close, DEMA1, 5)))
			{
				ExitLong(Convert.ToInt32(Size2), @"ForceExit 2", @"Pos2");
				ExitLong(Convert.ToInt32(Size1), @"ForceExit 1", @"Pos1");
			}
			
			 // Set 3
			if (
				 // Market Position -- Short
				(Position.MarketPosition == MarketPosition.Short)
				 && (CrossAbove(Close, DEMA1, 5)))
			{
				ExitShort(Convert.ToInt32(Size2), @"ForceExit 2", @"Pos2");
				ExitShort(Convert.ToInt32(Size1), @"ForceExit 1", @"Pos1");
			}
			
			 // Set 4
			if (
				 // Market Position : Flat
				(Position.MarketPosition == MarketPosition.Flat)
				 && (CrossAbove(Close, DEMA1, 5)))
			{
				EnterLongStopMarket(Convert.ToInt32(Size1), (High[0] + (1 * TickSize)) , @"Pos1");
				EnterLongStopMarket(Convert.ToInt32(Size2), (High[0] + (1 * TickSize)) , @"Pos2");
			}
			
			 // Set 5
			if (
				 // Market Position : Flat
				(Position.MarketPosition == MarketPosition.Flat)
				 && (CrossBelow(Close, DEMA1, 5)))
			{
				EnterShortStopMarket(Convert.ToInt32(Size1), (Low[0] + (-1 * TickSize)) , @"Pos1");
				EnterShortStopMarket(Convert.ToInt32(Size2), (Low[0] + (-1 * TickSize)) , @"Pos2");
			}
			
			 // Set 6
			if ((Position.MarketPosition != MarketPosition.Flat)
				 && (BarsSinceEntryExecution(0, "", 0) == 5))
			{
				Print(Convert.ToString(Times[0][0]) + " " + Convert.ToString(BarsSinceEntryExecution(0, "", 0)) + @"  bars passed !!!");
			}
			
		}

		#region Properties
		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="Target1", Description="First Target", Order=1, GroupName="Parameters")]
		public int Target1
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="Target2", Description="Second Target", Order=2, GroupName="Parameters")]
		public int Target2
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="Stop", Description="Stoploss entry", Order=3, GroupName="Parameters")]
		public int Stop
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="Size1", Description="First target Quantity", Order=4, GroupName="Parameters")]
		public int Size1
		{ get; set; }

		[NinjaScriptProperty]
		[Range(1, int.MaxValue)]
		[Display(Name="Size2", Description="Second Target quantity", Order=5, GroupName="Parameters")]
		public int Size2
		{ get; set; }
		#endregion

	}
